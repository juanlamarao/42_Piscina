void	buildpuzzlezero(int puzzle[6][6])
{
	int x;
	int y;

	y = 0;
	x = 0;
	while (x <= 5)
	{
		while (y <= 5)
		{
			puzzle[x][y] = 0;
			y++;
		}
		y = 0;
		x++;
	}
}
