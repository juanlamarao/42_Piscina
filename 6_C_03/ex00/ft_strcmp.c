int	ft_strcmp(char *s1, char *s2)
{
	int n;
	int r;

	n = 0;
	while ((s1[n]) || (s2[n]))
	{
		if (s1[n] != s2[n])
		{
			r = s1[n] - s2[n];
			return (r);
		}
		n++;
	}
	return (0);
}
