#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char letter;

	letter = 96;
	while (letter++ < 122)
	{
		ft_putchar(letter);
	}
}
